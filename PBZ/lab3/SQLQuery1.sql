CREATE TABLE Товары (
    "№Товара" INT PRIMARY KEY,
    Наименование VARCHAR(255),
    Единица_Измерения VARCHAR(50),
    Цена DECIMAL(10, 2)
);

CREATE TABLE Накладные (
    "№Накладной" INT PRIMARY KEY,
    "№Товара" INT,
    Количество INT,
    Стоимость DECIMAL(10, 2),
    FOREIGN KEY ("№Товара") REFERENCES Товары("№Товара")
);

CREATE TABLE Информация_О_Накладных (
    "№Накладной" INT,
    Ответственное_Лицо VARCHAR(255),
    Дата_Отгрузки DATE,
    Средний_Балл DECIMAL(3, 2),
    PRIMARY KEY ("№Накладной"),
    FOREIGN KEY ("№Накладной") REFERENCES Накладные("№Накладной")
);

